#include <bits/stdc++.h>
using namespace std;
int main(){
    for(int k=1;k<=10;k++){
        cout<<"\t"<<k;
        
    }
    cout<<"\n";
    
    for(int i=1;i<=10;i++){
        cout<<i;
        for(int j=1;j<=10;j++){
        
            cout<<"\t"<<i*j;
            
        }
        cout<<endl;
    }
    
}